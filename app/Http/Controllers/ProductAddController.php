     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
         $product_add = new ProductAdd();
         $product_add->name = $request->name;
         $product_add->amount = $request->amount;
         $product_add->stock = $request->stock;
         $product_add->description = $request->description;
         $product_add->photo = $request->image;
         $product_add->save();

         return back()->withSuccess('successfull add');
    }

